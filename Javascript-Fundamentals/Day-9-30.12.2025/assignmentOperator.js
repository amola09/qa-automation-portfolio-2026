/*  2. Assignment Operators: =, +=, -=, *=, /=, %=, **= (short hand methods) */

// assigny operator: =
let a = 10;
console.log(a)

// += : + and equal to
a = a + 5;
a += 5
a + 5
a = 15
console.log(a) // 15

// -= : - and equal to
a = a - 5;
a -= 5;
console.log(a); // 5

// *= :  * and equal to


a = a * 2
a *= 2
a * 2
a = 20
console.log(a); // 20


