// if-else:

if (5 != 5) {
    console.log("I am True");
} else {
    console.log("I am False");
}


// if else-if:

if (5 != 5) {
    console.log("I am True");
} else if (5 > 3) {
    console.log("I am else-if True");
}

// nested if-else:

if (5 != 3) {
    console.log("I am True");

    if (true) {
        console.log("I am nested True");
    }

} else {
    console.log("I am False");
}

